public class p4_addition {
    public static void main (String args []){
        int num1 = 10;
        int num2 = 20;
        
        System.err.println("addition :" + (num1+num2));
        System.err.println("addition :" + (num1-num2));
        System.err.println("addition :" + (num1*num2));
        System.err.println("addition :" + (num1/num2));
        System.err.println("addition :" + (num1%num2));
    }
}
